package Parte6;


public class NaveEspacial extends Pelota{
    private String armas;
    private String capacidad;
    private String potenciador;
    private double combustible;
    private String tipoComb;

    public NaveEspacial() {
    }

    public String getArmas() {
        return armas;
    }

    public void setArmas(String armas) {
        this.armas = armas;
    }

    public String getCapacidad() {
        return capacidad;
    }

    public void setCapacidad(String capacidad) {
        this.capacidad = capacidad;
    }

    public String getPotenciador() {
        return potenciador;
    }

    public void setPotenciador(String potenciador) {
        this.potenciador = potenciador;
    }

    public double getCombustible() {
        return combustible;
    }

    public void setCombustible(double combustible) {
        this.combustible = combustible;
    }

    public String getTipoComb() {
        return tipoComb;
    }

    public void setTipoComb(String tipoComb) {
        this.tipoComb = tipoComb;
    }
    
}
