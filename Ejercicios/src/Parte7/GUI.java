    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Retirar en cuenta de cheques
        double cantidad = Double.parseDouble(jTextField1.getText());
        if (cantidad < 0) {
            cuentaCheque.retirar(Double.parseDouble(jTextField1.getText()) * -1);
            jLabel1.setText(cuentaCheque.getBalance() + "");
        } else {
            JOptionPane.showMessageDialog(null, "La cantidad debe ser negativa", "Alert", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

