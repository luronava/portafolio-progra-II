    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Retirar en cuenta de ahorros
        double cantidad = Double.parseDouble(jTextField2.getText());
        if (cantidad <= 0) {
            if (cuentaAhorro.retirar(cantidad * -1)) {
                jLabel2.setText(cuentaAhorro.getBalance() + "");
            } else {
                JOptionPane.showMessageDialog(null, "El retiro excede de la cantidad disponible", "Alert", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "La cantidad debe ser negativa", "Alert", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

